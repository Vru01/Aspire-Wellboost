  // Defensive guard clause against undefined values to stop white screen crashes
  if (!backendProduct) return null;

  const targetSearchKey = backendProduct.slug || backendProduct._id || backendProduct.id;
  const values = Object.values(productCatalog || {});

  const staticBlock = values.find(p => 
    (p.slug && String(p.slug).toLowerCase() === String(targetSearchKey).toLowerCase()) || 
    (p.id && String(p.id).toLowerCase() === String(targetSearchKey).toLowerCase())
  );

  // Fallback cleanly to prevent rendering engine breakdowns if database records are out of sync
  const fallbackBlock = staticBlock || values[0] || {
    name: backendProduct.name || "Premium Wellness Stack",
    category: backendProduct.category || "General Supplements",
    summary: "High-performance health formulation.",
    badge: "Lab Certified Pure",
    imageGallery: ["/placeholder.jpg"],
    highlights: [],
    sections: []
  };

  return {
    ...fallbackBlock,
    ...backendProduct,
    id: backendProduct._id || backendProduct.id || fallbackBlock.id,
    price: backendProduct.price || fallbackBlock.price || 0,
    discountPrice: backendProduct.discountPrice || fallbackBlock.discountPrice || 0,
    name: backendProduct.name || fallbackBlock.name,
    category: backendProduct.category || fallbackBlock.category,
    imageGallery: fallbackBlock.imageGallery && fallbackBlock.imageGallery.length > 0 
      ? fallbackBlock.imageGallery 
      : [backendProduct.image || "/placeholder.jpg"]
  };
